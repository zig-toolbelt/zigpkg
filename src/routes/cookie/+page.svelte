  <h2>1. What Are Cookies?</h2>
  <p>
    Cookies are small text files that a website stores on your device when you visit. They
    allow the site to remember information about your visit — for example, your preferred
    language or display settings — so you don't have to re-enter it on your next visit.
    Cookies can be <em>session cookies</em> (deleted when you close your browser) or
    <em>persistent cookies</em> (stored for a set period or until you delete them).
  </p>

  <h2>2. Cookies We Use</h2>

  <h3>Strictly Necessary Cookies</h3>
  <p>
    These cookies are essential for ZigPkg to function correctly and cannot be disabled.
    They include:
  </p>
  <ul>
    <li>
      <strong>Session cookie</strong> — keeps track of your current session and selected
      preferences (e.g. language). This is a session cookie and is deleted when you close
      your browser.
    </li>
    <li>
      <strong>CSRF token</strong> — a security token that protects form submissions from
      cross-site request forgery attacks.
    </li>
  </ul>
  <p>
    Because these cookies are strictly necessary, they do not require your consent. Without
    them, the site cannot operate correctly.
  </p>

  <h3>Analytics Cookies</h3>
  <p>
    We may use privacy-friendly, anonymised analytics to understand how visitors use the
    site — for example, which packages are most popular or which search queries are most
    common. Any analytics data collected:
  </p>
  <ul>
    <li>Does not contain personally identifiable information.</li>
    <li>Is never shared with advertising networks.</li>
    <li>Is used only to improve the ZigPkg service.</li>
  </ul>
  <p>
    You can opt out of analytics cookies at any time using the controls in your browser
    (see Section 4 below).
  </p>

  <h3>No Third-Party Advertising Cookies</h3>
  <p>
    ZigPkg does not use advertising cookies. We do not display targeted ads and we do not
    share cookie data with any advertising networks or data brokers.
  </p>

