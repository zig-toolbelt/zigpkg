<div class="max-w-4xl mx-auto prose prose-slate">
  <p class="lead">
    ZigPkg ("we", "our", or "us") operates an open-source package registry for the
    <a href="https://ziglang.org">Zig</a> programming language at <strong>zigpkg.dev</strong>.
    This Privacy Policy describes what information we collect, why we collect it, and how we use it.
  </p>

  <h2>1. Information We Collect</h2>

  <h3>Package and Repository Metadata</h3>
  <p>
    ZigPkg indexes publicly available Zig packages from GitHub. We collect and store metadata
    that is already public, including repository names, descriptions, topics, star counts,
    version tags, and dependency information. We do not access private repositories.
  </p>

  <h3>Usage Data</h3>
  <p>
    We may collect aggregate, anonymised analytics about how the site is used — such as
    page views and search queries — to understand which features are most useful.
    This data is never sold and is not linked to individual users.
  </p>

  <h3>Log Data</h3>
  <p>
    Like most web services, our hosting provider automatically records standard server log
    information such as IP addresses, browser type, referring URL, and the date and time of
    your request. These logs are retained for a short period for security and debugging
    purposes and are then discarded.
  </p>

  <h3>Cookies</h3>
  <p>
    We use a minimal number of cookies. Please see our
    <a href="/cookie">Cookie Policy</a> for full details.
  </p>

  <h2>2. How We Use Information</h2>
  <p>We use the information we collect to:</p>
  <ul>
    <li>Display and search Zig packages on the registry.</li>
    <li>Keep package metadata up to date by syncing with public GitHub repositories.</li>
    <li>Diagnose technical problems and improve the reliability of the service.</li>
    <li>Understand aggregate usage patterns to prioritise development work.</li>
  </ul>
  <p>We do not sell, trade, or rent your personal information to third parties.</p>

  <h2>3. Third-Party Services</h2>

  <h3>GitHub API</h3>
  <p>
    ZigPkg uses the GitHub REST API to fetch public package data. Your use of ZigPkg
    does not grant us access to your GitHub account. GitHub's own
    <a href="https://docs.github.com/en/site-policy/privacy-policies/github-general-privacy-statement" rel="noopener noreferrer" target="_blank">
      Privacy Statement
    </a>
    applies to data hosted on GitHub.
  </p>

  <h3>Hosting Infrastructure</h3>
  <p>
    The ZigPkg website is hosted on third-party cloud infrastructure. Our hosting provider
    may process request logs as described in their own privacy policies. We choose providers
    that adhere to modern data-protection standards.
  </p>

  <h2>4. Data Retention</h2>
  <p>
    Package metadata is retained as long as the corresponding public repository exists and
    is indexed by ZigPkg. Server logs are kept for no longer than 30 days. If you believe
    your repository has been incorrectly indexed, please open an issue on our
    <a href="https://github.com/zig-toolbelt/zigpkg" rel="noopener noreferrer" target="_blank">
      GitHub repository
    </a>.
  </p>

  <h2>5. Children's Privacy</h2>
  <p>
    ZigPkg does not knowingly collect personal information from children under the age of 13.
    The service is a developer tool and is not directed at children.
  </p>

  <h2>6. Changes to This Policy</h2>
  <p>
    We may update this Privacy Policy from time to time. Changes will be reflected by updating
    the "Last updated" date at the top of this page. Material changes will also be announced
    in our
    <a href="https://github.com/zig-toolbelt/zigpkg" rel="noopener noreferrer" target="_blank">
      GitHub repository
    </a>.
  </p>

  <h2>7. Contact</h2>
  <p>
    ZigPkg is an open-source project. If you have questions or concerns about this Privacy
    Policy, please open an issue or start a discussion on our
    <a href="https://github.com/zig-toolbelt/zigpkg" rel="noopener noreferrer" target="_blank">
      GitHub repository
    </a>.
  </p>
